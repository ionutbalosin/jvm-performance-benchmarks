package com.ionutbalosin.jvm.performance.benchmarks.micro.compiler;

import java.util.concurrent.TimeUnit;
import org.openjdk.jmh.annotations.Benchmark;
import org.openjdk.jmh.annotations.BenchmarkMode;
import org.openjdk.jmh.annotations.Fork;
import org.openjdk.jmh.annotations.Measurement;
import org.openjdk.jmh.annotations.Mode;
import org.openjdk.jmh.annotations.OutputTimeUnit;
import org.openjdk.jmh.annotations.Param;
import org.openjdk.jmh.annotations.Scope;
import org.openjdk.jmh.annotations.State;
import org.openjdk.jmh.annotations.Warmup;

/*
 * Check if the Compiler can recognize the existence of the induction variables and to replace it with simpler computations.
 * This optimization is a special case of strength reduction where all loop iterations are strengthened to a mathematical formula.
 *
 * References:
 * - https://llvm.org/devmtg/2009-10/ScalarEvolutionAndLoopOptimization.pdf
 */
@BenchmarkMode(Mode.AverageTime)
@OutputTimeUnit(TimeUnit.NANOSECONDS)
@Warmup(iterations = 5, time = 10, timeUnit = TimeUnit.SECONDS)
@Measurement(iterations = 5, time = 10, timeUnit = TimeUnit.SECONDS)
@Fork(value = 5)
@State(Scope.Thread)
public class ScalarEvolutionAndLoopOptimizationBenchmark {

  // $ java -jar */*/benchmarks.jar ".*ScalarEvolutionAndLoopOptimizationBenchmark.*"

  @Param({"16384"})
  private int size;

  @Benchmark
  public int scev_loop() {
    int sum = 0;
    for (int i = 0; i < size; i++) {
      sum += i;
    }
    return sum;
  }

  @Benchmark
  public int baseline() {
    return ((size * (size + 1)) / 2);
  }
}
